// Módulo (%)
// Operador binário. Retorna o inteiro restante da divisão dos dois operandos.

12 % 5 // retorna 2.

// Incremento (++) e Decremento (--)   Operador Unario
++x
x++

const a = ++2; // 3     incrimenta na hora
const b = 2++; // 2     retorna o valor depois incrementa

--x 
x--

// Negação (-) e Adição (+)
// + força o valor de uma string para numerico.
-3
+"3"        // retorna 3
+true       // retorna 1
+false      // retorna 0
-true       // retorna -1

// Operador de exponenciação (**)
2 ** 3      // retorna 8
10 ** -1    // retorna 0.1

// Operador de agrupamento ()
2 * (3 + 2)